#!/usr/bin/env node
/**
 * Regenerates docs/skills-and-agents.md (same role as the former Ruby script).
 * Run: node scripts/document-skills-and-agents.mjs [--check]
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import YAML from "yaml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const OUTPUT = path.join(ROOT, "docs", "skills-and-agents.md");

function skillDirs() {
  const base = path.join(ROOT, ".claude", "skills");
  if (!fs.existsSync(base)) {
    return [];
  }
  return fs
    .readdirSync(base, { withFileTypes: true })
    .filter((d) => d.isDirectory())
    .map((d) => d.name)
    .sort()
    .map((folder) => ({
      folder,
      skillMd: path.join(base, folder, "SKILL.md"),
    }))
    .filter((x) => fs.existsSync(x.skillMd));
}

function parseSkillFrontmatter(skillPath) {
  const raw = fs.readFileSync(skillPath, "utf8");
  if (!raw.startsWith("---\n")) {
    return null;
  }
  const parts = raw.split(/^---\s*$/m);
  if (parts.length < 3) {
    return null;
  }
  try {
    const data = YAML.parse(parts[1]);
    if (!data || typeof data !== "object") {
      return null;
    }
    const name = String(data.name ?? "").trim();
    let desc = data.description;
    desc = typeof desc === "string" ? desc.trim().replace(/\s+/g, " ") : "";
    return { name: name || path.basename(path.dirname(skillPath)), description: desc };
  } catch {
    return {
      name: path.basename(path.dirname(skillPath)),
      description: "(could not parse frontmatter)",
    };
  }
}

function ciJobs() {
  const p = path.join(ROOT, ".github", "workflows", "ci.yml");
  if (!fs.existsSync(p)) {
    return [];
  }
  try {
    const y = YAML.parse(fs.readFileSync(p, "utf8"));
    if (y && typeof y === "object" && y.jobs && typeof y.jobs === "object") {
      return Object.keys(y.jobs).sort();
    }
  } catch {
    /* ignore */
  }
  return [];
}

function dependabotEcosystems() {
  const p = path.join(ROOT, ".github", "dependabot.yml");
  if (!fs.existsSync(p)) {
    return [];
  }
  try {
    const y = YAML.parse(fs.readFileSync(p, "utf8"));
    if (!y || !Array.isArray(y.updates)) {
      return [];
    }
    const eco = y.updates.map((u) => u["package-ecosystem"]).filter(Boolean);
    return [...new Set(eco)].sort();
  } catch {
    return [];
  }
}

function sentryInWebApp() {
  const p = path.join(ROOT, "apps", "web", "package.json");
  if (!fs.existsSync(p)) {
    return false;
  }
  try {
    const pkg = JSON.parse(fs.readFileSync(p, "utf8"));
    const deps = { ...pkg.dependencies, ...pkg.devDependencies };
    return Object.keys(deps).some((k) => k.startsWith("@sentry/"));
  } catch {
    return false;
  }
}

function buildMarkdown() {
  const lines = [];
  lines.push("# Skills and automation inventory");
  lines.push("");
  lines.push("<!-- AUTO-GENERATED by scripts/document-skills-and-agents.mjs — do not edit by hand. -->");
  lines.push("<!-- Run `yarn document-skills` or `node scripts/document-skills-and-agents.mjs` after adding skills. -->");
  lines.push("");
  // Intentionally no timestamp — keeps `yarn document-skills` + `--check` deterministic in CI.
  lines.push("");
  lines.push(
    "Human-readable guides: **[`docs/agents.md`](agents.md)** (automation), **[`CLAUDE.md`](../CLAUDE.md)** (Claude Code context).",
  );
  lines.push("");
  lines.push("## Claude Code skills");
  lines.push("");
  lines.push(
    "Each row is a folder under `.claude/skills/<folder>/` with a `SKILL.md` file. Descriptions come from YAML frontmatter.",
  );
  lines.push("");

  const rows = skillDirs()
    .map(({ folder, skillMd }) => {
      const fm = parseSkillFrontmatter(skillMd);
      if (!fm) {
        return null;
      }
      return [folder, fm.name, fm.description];
    })
    .filter(Boolean);

  if (rows.length === 0) {
    lines.push("*(No skills found under `.claude/skills/`.)*");
    lines.push("");
  } else {
    lines.push("| Folder | `name` (frontmatter) | Description |");
    lines.push("|--------|------------------------|-------------|");
    for (const [folder, name, desc] of rows) {
      lines.push(`| \`${folder}\` | ${name} | ${String(desc).replace(/\|/g, "\\|")} |`);
    }
    lines.push("");
  }

  lines.push("## Automation (agents)");
  lines.push("");
  lines.push(
    "These entries are **derived from config files**; narrative detail stays in **[`docs/agents.md`](agents.md)**.",
  );
  lines.push("");

  const jobs = ciJobs();
  if (jobs.length) {
    lines.push("### GitHub Actions CI jobs (`ci.yml`)");
    lines.push("");
    lines.push("| Job |");
    lines.push("|-----|");
    for (const j of jobs) {
      lines.push(`| \`${j}\` |`);
    }
    lines.push("");
  } else {
    lines.push("### GitHub Actions");
    lines.push("");
    lines.push("*(Could not read `.github/workflows/ci.yml`.)*");
    lines.push("");
  }

  const eco = dependabotEcosystems();
  if (eco.length) {
    lines.push("### Dependabot (`dependabot.yml`)");
    lines.push("");
    lines.push(`Package ecosystems: ${eco.map((e) => `\`${e}\``).join(", ")}.`);
    lines.push("");
  }

  lines.push("### Optional Sentry");
  lines.push("");
  if (sentryInWebApp()) {
    lines.push(
      "`@sentry/*` is listed in `apps/web/package.json`. Set **`SENTRY_DSN`** in production — see `docs/agents.md`.",
    );
  } else {
    lines.push("*(No `@sentry/*` dependency detected in `apps/web`.)*");
  }
  lines.push("");

  lines.push("## Regenerating this file");
  lines.push("");
  lines.push("```bash");
  lines.push("yarn document-skills");
  lines.push("# or: node scripts/document-skills-and-agents.mjs");
  lines.push("```");
  lines.push("");
  lines.push(
    "In CI, the **`docs_inventory`** job runs `node scripts/document-skills-and-agents.mjs --check` and fails if `docs/skills-and-agents.md` does not match the generator output.",
  );
  lines.push("");

  return lines.join("\n");
}

const check = process.argv.includes("--check");
const md = buildMarkdown();

// In --check mode, compare the committed file on disk to what we would write.
// (We do not write first: that made `yarn document-skills && ... --check` fail until
// `git add`, and it mutated the tree even when the file was already correct.)
if (check) {
  const existing = fs.existsSync(OUTPUT) ? fs.readFileSync(OUTPUT, "utf8") : "";
  if (existing !== md) {
    console.error("docs/skills-and-agents.md is out of date. Run: yarn document-skills");
    process.exit(1);
  }
  console.log(`${OUTPUT} is up to date.`);
  process.exit(0);
}

fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
fs.writeFileSync(OUTPUT, md);
console.log(`Wrote ${OUTPUT}`);
