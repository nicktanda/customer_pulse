  def objective_status_color(status)
    case status
    when "active"
      "text-green-600"
    when "achieved"
      "text-blue-600"
    when "paused"
      "text-yellow-600"
    when "abandoned"
      "text-gray-500"
    else
      "text-gray-600"
    end
  end

